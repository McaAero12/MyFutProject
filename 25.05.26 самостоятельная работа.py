#Базовый класс Animal(Родитель)
class Animal:
    def __init__(self, name, age, health):
        self.name = name
        self.age = age
        # Инкапсуляция: защищенное поле _health
        # Мы помечаем его нижним подчеркиванием, чтобы показать,
        # что его нельзя менять напрямую извне.
        self._health = health
    def get_health(self):
        return self._health
# Метод для вывода общей информации
    def info(self):
        print(f"Животное:{self.name}, возраст:{self.age}, здоровье:{self._health}")

#наследование:LION наследует все от Animal
class Lion(Animal):
    def roar(self):
        print(f"Лев {self.name} громко рычит!,гости в восторге")

    # Взаимодействие меняет защищенное поле _health
    def feed(self, amount):
        self._health += amount
        print(f"сотрудник покормил льва {self.name} .Здоровье увеличелось на {amount}")
# Наследование: Penguin наследует все от Animal
class Penguin(Animal):
    # Уникальный метод для Пингвина
    def swim(self):
        self._health += 5
        print(f"{self.name} поплавал и стал здоровее!")

# Создаем объекты
simba = Lion("Симба", 6, 80)
pingu = Penguin("Пороро", 2, 90)

simba.info()
pingu.info()

simba.roar()
simba.feed(10)
pingu.swim()

print(f"Итоговое здоровье льва {simba.name} теперь {simba.get_health()}")
print(f"Итоговое здоровье пингвина {pingu.name} теперь {pingu.get_health()}")

