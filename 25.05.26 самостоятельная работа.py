#Описание:
# Это модель симулятора ухода за животными,
# созданная на основе ООП. В системе питомцы — это объекты с защищёнными параметрами (здоровье),
# а пользователь управляет ими через методы взаимодействия.
# Это имитирует механику игр типа Zoo Tycoon или Tamagotchi,
# где здоровье питомца меняется только в результате целевых действий, что обеспечивает целостность данных системы.

#2
# Animal (Родительский класс): является фундаментом всей системы.
# Он хранит общие данные всех животных: имя (name), возраст (age) и защищённый уровень здоровья (_health).
# Lion (Дочерний класс): наследует все атрибуты Animal, но расширяет его функционал уникальным методом
# roar() и методом feed(), который отвечает за взаимодействие и изменение состояния здоровья.
# # Penguin (Дочерний класс): также наследует всё от Animal
# и добавляет собственный метод swim(),
# который позволяет пингвину взаимодействовать с окружающей средой и восстанавливать здоровье.


#3
# Защищенное поле: _health (уровень здоровья животного).
#  Как защищено: Поле защищено использованием нижнего подчёркивания _health
# В Python это соглашение, указывающее на то,
# что поле является «внутренним» и к нему не следует обращаться напрямую из внешнего кода.


#4
# Особые методы и фишки
# Уникальность: Разные действия для разных животных (roar для льва, swim для пингвина).
# Интерактивность: Метод feed() не просто меняет число, а имитирует уход за питомцем.
# Профессиональный подход: Использование геттера (get_health) для безопасного чтения данных вместо обращения к переменной напрямую.

